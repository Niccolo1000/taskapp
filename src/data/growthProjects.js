export const getProjectById = (id) => growthProjects.find(project => project.id === id);

export const getProjectsByType = (type) => growthProjects.filter(project => project.type === type);

export const getProjectsByStatus = (status) => growthProjects.filter(project => project.status === status);

export const getCompletedProjects = () => growthProjects.filter(project => project.status === "completed");

export const getInProgressProjects = () => growthProjects.filter(project => project.status === "in-progress");

export const getPlannedProjects = () => growthProjects.filter(project => project.status === "planned");

export const getTotalTreesPlanted = () =>
  growthProjects
    .filter(p => p.treesPlanted)
    .reduce((sum, p) => sum + p.treesPlanted, 0);

// Growth stats summary
export const getGrowthStats = () => ({
  totalProjects: growthProjects.length,
  completedProjects: getCompletedProjects().length,
  inProgressProjects: getInProgressProjects().length,
  plannedProjects: getPlannedProjects().length,
  totalTreesPlanted: getTotalTreesPlanted(),
  projectTypes: {
    treePlanting: getProjectsByType("tree-planting").length,
    greenRoof: getProjectsByType("green-roof").length,
    rewilding: getProjectsByType("rewilding").length,
    communityGarden: getProjectsByType("community-garden").length,
    greenInfrastructure: getProjectsByType("green-infrastructure").length
  }
});
